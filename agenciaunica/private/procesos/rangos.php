            function toggleTab(evt, tabName) {
                var i, tabcontent, tablinks, subtabcontent;
                tabcontent = document.getElementsByClassName("tabcontent");
                tablinks = document.getElementsByClassName("tablinks");
                subtabcontent = document.getElementsByClassName("subtabcontent");

                // Si la pestaña ya está activa, se cierra
                if (evt.currentTarget.classList.contains("active")) {
                    evt.currentTarget.classList.remove("active");
                    document.getElementById(tabName).style.display = "none";
                    return;
                }

                // De lo contrario, se abre la pestaña seleccionada
                for (i = 0; i < tabcontent.length; i++) {
                    tabcontent[i].style.display = "none";
                }
                for (i = 0; i < tablinks.length; i++) {
                    tablinks[i].className = tablinks[i].className.replace(" active", "");
                }
                document.getElementById(tabName).style.display = "block";
                evt.currentTarget.className += " active";

                // Ocultar todas las subtabs cuando se selecciona una nueva tab principal
                for (i = 0; i < subtabcontent.length; i++) {
                    subtabcontent[i].style.display = "none";
                    subtabcontent[i].classList.remove("active");
                }
            }

            function openSubTab(evt, subTabName) {
                var i, subtabcontent, subtablinks;
                subtabcontent = document.getElementsByClassName("subtabcontent");
                for (i = 0; i < subtabcontent.length; i++) {
                    subtabcontent[i].style.display = "none";
                }
                subtablinks = document.getElementsByClassName("subtablinks");
                for (i = 0; i < subtablinks.length; i++) {
                    subtablinks[i].className = subtablinks[i].className.replace(" active", "");
                }
                document.getElementById(subTabName).style.display = "block";
                evt.currentTarget.className += " active";
            }

            // No inicializar ninguna pestaña por defecto
            document.addEventListener("DOMContentLoaded", function() {
                // No hacer clic en ninguna pestaña por defecto
            });
